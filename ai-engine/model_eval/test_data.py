def remove_similar_entries(df, content_col='question', threshold=0.90, preview=True):
    """
    TF-IDF ê¸°ë°˜ ìœ ì‚¬ í•­ëª© ì œê±° í•¨ìˆ˜
    - titleê³¼ contentë¥¼ í•©ì³ ìœ ì‚¬ë„ ê³„ì‚°
    - ìœ ì‚¬ë„ê°€ threshold ì´ìƒì¸ í•­ëª©ì€ ì œê±°
    - ì¤‘ë³µ í›„ë³´ ë¯¸ë¦¬ë³´ê¸°ë„ ê°€ëŠ¥
    """
    df = df.reset_index(drop=True).copy()
    
    # TF-IDF ë²¡í„°í™”
    vectorizer = TfidfVectorizer().fit_transform(df[content_col])
    similarity_matrix = cosine_similarity(vectorizer)

    to_drop = set()
    duplicate_pairs = []

    for i in range(similarity_matrix.shape[0]):
        for j in range(i + 1, similarity_matrix.shape[0]):
            sim = similarity_matrix[i, j]
            if sim > threshold:
                to_drop.add(j)
                duplicate_pairs.append((i, j, sim))

    if preview:
        print("ğŸŸ¡ ìœ ì‚¬ë„ê°€ ë†’ì€ í•­ëª© ìŒ:")
        for i, j, sim in duplicate_pairs:
            print(f"\nğŸ”¹ [{i}] ì§ˆë¬¸: {df.loc[i]}")
            print(f"ğŸ”¹ [{j}] ì§ˆë¬¸: {df.loc[j]}")
            print(f"   â†ª ìœ ì‚¬ë„: {sim:.4f}")

    # ì¤‘ë³µ ì œê±°
    df_cleaned = df.drop(list(to_drop)).reset_index(drop=True)

    return df_cleaned

